def caesar(caesar_text, shift_amount, type):
    message = ""
    if (type.lower() == "decode"):
        shift_amount *= -1
    for char in caesar_text:
        if char in alphabet:
            index = alphabet.index(char)
            new_position = index + shift_amount    
            message += alphabet[new_position]
        else:
            message += char
    print(f"The {type.lower()}d message is: {message}")

should_continue = True   
